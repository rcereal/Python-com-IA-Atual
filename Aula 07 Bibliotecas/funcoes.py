def verificador_idade(idade):
    if idade >= 18:
        return 'Você é de maior!'
    else:
        return 'Você é de menor!'
    

def verificador_numeros(numero):
    if numero > 0:
        return 'O número é positivo!'
    elif numero < 0:
        return 'O número é negativo!'
    else:
        return 'O numero Zero é neutro!'
    

